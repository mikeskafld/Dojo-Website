/* â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
   1.  create or update Polar products â†’ plans
   â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€ */
async function syncPolarPlans() {
  console.log("â³  Creating / syncing Polar productsâ€¦")

  const plans = [
    { code: "base", name: "Base", price: 800, seatLimit: 5 },
    { code: "plus", name: "Plus", price: 1200, seatLimit: 10 },
  ] as const

  for (const p of plans) {
    // create the product in Polar
    const product = await polar.products.create({
      organizationId: process.env.POLAR_ORG_ID!,
      name: p.name,
      recurringInterval: "month",
      prices: [
        {
          amountType: "fixed",
          priceAmount: p.price,
          priceCurrency: "usd",
        },
      ],
    })

    console.log(`  â€¢ plan '${p.code}' â†” Polar product ${product.id}`)
  }
}

